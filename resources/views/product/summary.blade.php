<div class="description">
    <div class="description-title">Descripción:</div>
    Super Mario Odyssey es un videojuego de plataformas de mundo abierto para Nintendo Switch
    que se lanzó el 27 de octubre de 2017.
    Es el decimonoveno título de la serie Super Mario y el séptimo juego de plataformas en 3D de Mario.
    También es el tercer juego de Super Mario en esta consola, ya que el primero fue Mario Kart 8 Deluxe,
    y el segundo Mario + Rabbids Kingdom Battle.
    Este juego tiene una clasificación para mayores de 7 años en Europa y para 10 en Rusia y Estados Unidos,
    siendo el primer juego oficial de la saga en tener tales clasificaciones.
    A solo unas cuantas horas previas a su lanzamiento oficial, ya había sido considerado por los críticos
    especializados como una obra maestra y como uno de los mejores videojuegos de todos los tiempos.
